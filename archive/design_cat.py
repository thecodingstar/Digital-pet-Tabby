def render(frames, scale=22, path="preview_cat.png"):
    W = 16 * scale
    img = QImage(W * len(frames) + 10 * (len(frames) + 1), 16 * scale + 20,
                 QImage.Format_ARGB32)
    img.fill(QColor("#202830"))
    p = QPainter(img)
    for fi, g in enumerate(frames):
        ox = 10 + fi * (W + 10)
        for ri, row in enumerate(g):
            for ci, ch in enumerate(row):
                col = PALETTE.get(ch)
                if col:
                    p.fillRect(ox + ci * scale, 10 + ri * scale, scale, scale,
                               QColor(col))
    p.end()
    img.save(path)
    print("wrote", path)


if __name__ == "__main__":
    app = QApplication(sys.argv)
    render([IDLE1, IDLE2, WALK1, WALK2])
