CHARACTERS = ["black", "tabby", "ginger", "cream"]
HEAT_CHARS = ("F", "f")


def compose(name, state, leg_phase=0, face_override=None):
    """Return a 16x16 grid of chars for (character, state)."""
    body = [row[:] for row in BODIES[name]]
    if state in WALK_STATES:
        legs = walk_legs(name)[leg_phase % 2]
        for i, r in enumerate((13, 14)):
            if i < len(legs):
                body[r] = legs[i][:]
    fname = face_override or STATE_FACE.get(state, "neutral")
    face = FACES.get(fname, FACES["neutral"])
    for fr, frow in enumerate(face):
        br = FACE_TOP + fr
        for ci, ch in enumerate(frow):
            if ch != ".":
                body[br][ci] = ch
    return body
